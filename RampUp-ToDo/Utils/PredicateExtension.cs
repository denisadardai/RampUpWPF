namespace RampUp_ToDo.Utils
{
    public static class PredicateExtension
    {
        public static Func<T, bool> AND<T>(params Predicate<T>[] predicates)
        {
            return delegate (T item)
            {
                foreach (Predicate<T> predicate in predicates)
                {
                    if (!predicate(item))
                    {
                        return false;
                    }
                }
                return true;
            };
        }
    }
}
